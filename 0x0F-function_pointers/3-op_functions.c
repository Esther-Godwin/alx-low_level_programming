/**
 * op_add - Adds two numbers
 * @a: First number
 * @b: Second number
 *
 * Return: The sum of the two numbers
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Subtracts teo numbers
 * @a: First number
 * @b: Second number
 *
 * Return: Difference of the two numbers
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - Multiplies two numbers
  * @a: First number
  * @b: Second number
  *
  * Return: Product of two numbers
  */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Divides two numbers
 * @a: First number
 * @b: Second number
 *
 * Return: Dividend of two numbers
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Get the remainder of a division
 * @a: First number
 * @b: Second number
 *
 * Return: Remainder of the division of two numbers
 */
int op_mod(int a, int b)
{
	return (a % b);
}
